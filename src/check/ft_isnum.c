#include "../../inc/push_swap.h"

int	ft_isnum(char *s)
{
	size_t	i;

	i = 0;
	if (s[0] == '-' && ft_isdigit(s[1]))
		i++;
	while (s[i])
	{
		if (!ft_isdigit(s[i]))
			return (0);
		i++;
	}
	return (i);
}
